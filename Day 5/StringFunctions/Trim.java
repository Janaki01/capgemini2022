package StringFunctions;

public class Trim {
		public static void main(String args[]){  
		String s1="   This is   ";  
		System.out.println(s1+"janaki");//without trim()  
		System.out.println(s1.length());
		String s2=s1.trim();
		System.out.println(s2+"janaki");
		System.out.println(s2.length());
		
		}

}
